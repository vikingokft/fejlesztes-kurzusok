#include <iostream>

int main() {

int number_1 = 10;

int number_2 = 3;

// összeadás

std::cout << number_1 << " + " << number_2 << " = " <<
number_1 + number_2 << std::endl;

// kivonás

std::cout << number_1 << " - " << number_2 << " = " <<
number_1 - number_2 << std::endl;

// szorzás

std::cout << number_1 << " * " << number_2 << " = " <<
number_1 * number_2 << std::endl;

}