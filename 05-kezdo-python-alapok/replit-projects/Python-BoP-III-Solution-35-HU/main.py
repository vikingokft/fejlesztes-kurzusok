def print_sandwich_recipe(condiment, meat, cheese, vegetable):
  print('1. Fogj két szelet kenyeret.')
  print('2. Kend meg az egyiket ' + condiment + ' szósszal.')
  print('3. Tegyél rá ' + meat + ' húst.')
  print('4. Borítsd meg ' + cheese + ' sajttal.')
  print('5. Dobj rá egy kis ' + vegetable + ' zöldséget.')
  print('6. Zárd le a másik szelet kenyérrel.')

print_sandwich_recipe('mustár', 'pulyka', 'cheddar', 'saláta')