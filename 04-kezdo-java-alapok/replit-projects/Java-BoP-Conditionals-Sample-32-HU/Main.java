class Main {

public static void main(String[] args) {

for (int i = 1; i <= 20; i++) {

if (i == 10) {

System.out.println(i + " is the prettiest even number.");

} else if (i % 2 != 0) {

System.out.println(i + " is an odd number.");

} else {

System.out.println(i + " is an even number.");

};

}

}

}