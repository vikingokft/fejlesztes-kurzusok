public class Cat {

String breed;

String colorOfFur;

double weight;

int age;

boolean hasToyMouse;

Cat(String breed, String colorOfFur, double weight, int age, boolean hasToyMouse) {

this.breed = breed;

this.colorOfFur = colorOfFur;

this.weight = weight;

this.age = age;

this.hasToyMouse = hasToyMouse;

}

}