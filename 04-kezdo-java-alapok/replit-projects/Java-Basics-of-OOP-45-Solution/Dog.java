public class Dog {

String breed;

String colorOfFur;

double weight;

int age;

boolean hasToyBone;

Dog(String breed, String colorOfFur, double weight, int age, boolean hasToyBone) {

this.breed = breed;

this.colorOfFur = colorOfFur;

this.weight = weight;

this.age = age;

this.hasToyBone = hasToyBone;

}

}